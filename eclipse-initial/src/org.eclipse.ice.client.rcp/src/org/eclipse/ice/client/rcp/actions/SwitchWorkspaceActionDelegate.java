package org.eclipse.ice.client.rcp.actions;

import org.eclipse.jface.action.IAction;
import org.eclipse.jface.viewers.ISelection;
import org.eclipse.ui.IWorkbenchWindow;
import org.eclipse.ui.IWorkbenchWindowActionDelegate;
import org.eclipse.ui.actions.ActionFactory.IWorkbenchAction;

/**
 * @author Jay Jay Billings
 * This class is used by Eclipse to launch a SwitchWorkspaceAction.
 */
public class SwitchWorkspaceActionDelegate implements
		IWorkbenchWindowActionDelegate {

	/**
	 * The active window in which the action should be run
	 */
	private IWorkbenchWindow activeWindow = null;
		
	/* (non-Javadoc)
	 * @see org.eclipse.ui.IActionDelegate#run(org.eclipse.jface.action.IAction)
	 */
	@Override
	public void run(IAction action) {

		// Local Declarations
		IWorkbenchAction switchWorkspaceAction = new SwitchWorkspaceAction(activeWindow);
		
		// Launch the action
		switchWorkspaceAction.run();

		return;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.IActionDelegate#selectionChanged(org.eclipse.jface.action.IAction, org.eclipse.jface.viewers.ISelection)
	 */
	@Override
	public void selectionChanged(IAction action, ISelection selection) {
		// TODO Auto-generated method stub

	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.IWorkbenchWindowActionDelegate#dispose()
	 */
	@Override
	public void dispose() {
		// TODO Auto-generated method stub

	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.IWorkbenchWindowActionDelegate#init(org.eclipse.ui.IWorkbenchWindow)
	 */
	@Override
	public void init(IWorkbenchWindow window) {
		activeWindow = window;
	}

}
